#include "libft.h"

int		ft_atoi(const char *str)
{
	long long int	nb;
	long long int	flag;
	int				i;

	nb = 0;
	flag = 1;
	i = 0;
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			flag = -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
		nb = nb * 10 + str[i++] - 48;
	return (nb * flag);
}
